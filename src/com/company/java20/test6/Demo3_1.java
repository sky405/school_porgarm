public class Demo3_1 {
    public static void main(String args[]) {
        byte byteVariable=0x55;
        short shortVariable=0x55ff;
        int intVariable=100000;
        long longVariable=0xffffL;
        char charVariable='a';
        float floatVariable=0.3f;
        double doubleVariable=0.7E-2;
        boolean booleanVariable=true;
        System.out.println("字节型变量 byteVariable = "+ byteVariable);
        System.out.println("短整型变量 shortVariable = "+ shortVariable);
     System.out.println("整型变量 intVariable = "+ intVariable);
    System.out.println("长整型变量 longVariable = "+ longVariable);
    System.out.println("字符型变量 charVariable = "+ charVariable);
    System.out.println("浮点型变量 floatVariable = "+ floatVariable);
    System.out.println("双精度变量 doubleVariable = "+ doubleVariable);
    System.out.println("布尔型变量 booleanVariable = "+ booleanVariable);
    }

}
